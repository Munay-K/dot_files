#!/bin/sh

# -------------------------------------------------------
# ///////////////////////////////////////////////////////
# Copying files to current directory
# ///////////////////////////////////////////////////////
# -------------------------------------------------------

# -------------------------------------------------------
# Ubuntu vanilla
# -------------------------------------------------------

cp -r ~/Documents/MyFiles/Scripts/ general/
i
# -------------------------------------------------------
# Vim files
# -------------------------------------------------------

cp -r ~/.vim/MySnippets vim/
cp ~/.vimrc vim/

# -------------------------------------------------------
# zsh files
# -------------------------------------------------------

cp ~/.zshrc zsh/

# -------------------------------------------------------
# tmux files
# -------------------------------------------------------

cp ~/.tmux.conf tmux/

# -------------------------------------------------------
# i3 files
# -------------------------------------------------------

cp ~/.config/i3 i3/

# -------------------------------------------------------
# ///////////////////////////////////////////////////////
# Pushing all files to remote repoository
# ///////////////////////////////////////////////////////
# -------------------------------------------------------

git add -A
git commit -m "Monthly backup"
git push origin master
